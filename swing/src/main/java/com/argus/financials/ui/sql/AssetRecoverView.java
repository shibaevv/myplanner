    public void messageSent(MessageSentEvent e) {
        System.out.println("AssetRecoverView::messageSent() " + e.getMessage());

        Object sender = e.getMessageSender(); // Object or Exception

        if (AssetManagerModel.ENABLE.equals(e.getMessage()))
            jButtonExecute.setEnabled(true);

        else if (AssetManagerModel.DISABLE.equals(e.getMessage()))
            jButtonExecute.setEnabled(false);

        else if (AssetManagerModel.UPDATE2.equals(e.getMessage())) {

            AssetProcessor.Journal batch = tableModelStep1.getSelectedBatch();
            if (batch == null) {
                tableStep2.setModel(new DefaultTableModel());
                jLabelStep2text.setText("");
                return;
            }

            tableStep2.setModel(getTableModelStep2(batch, true));
            // tableStep2.setModel( getTableModelStep2( ( AssetProcessor.Journal
            // ) sender, true ) );

            StringBuffer summary = new StringBuffer();
            summary.append("<HTML>");
            summary.append("<FONT SIZE=2><FONT FACE=\"Arial, sans-serif\"> ");
            summary
                    .append("<B><U>Restore from</B></U><BR> "
                            + batch.toString());
            // summary.append( "<BR>" );
            // summary.append( "<B><U>To</B></U><BR> " + model.getValue(
            // STEP_1_REPLACE_WITH ) );
            summary.append("</FONT></FONT>");
            summary.append("</HTML>");

            jLabelStep2text.setText(summary.toString());

        } else if (AssetManagerModel.UPDATE3.equals(e.getMessage())) {

            StringBuffer summary = new StringBuffer();
            summary.append("<HTML>");

            // init table model ( )
            if (sender instanceof Exception) {
                summary.append(((Exception) sender).getMessage());

                tableStep3.setModel(new DefaultTableModel());

            } else {
                summary.append(sender.toString() + ", successfully reversed!");

                // init ctable model ( list )
                SortedTableModel sortedTableModel = new SortedTableModel(
                        getTableModelStep1(true));
                tableStep1.setModel(sortedTableModel);
                setColumnModelStep1();
                sortedTableModel.addMouseListenerToHeaderInTable(tableStep1);

                tableStep3.setModel(getTableModelStep3(
                        (AssetProcessor.Journal) sender, true));

            }

            summary.append("</HTML>");
            jLabelStep3text.setText(summary.toString());

        }

    }

