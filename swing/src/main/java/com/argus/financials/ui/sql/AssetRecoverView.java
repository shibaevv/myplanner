    static class TableModelStep1 extends DefaultTableModel {

        private static final int SELECTED = 0;

        private static final int BATCH = 1;

        private static final int JOURNAL = 2;

        private static final int DATE = 3;

        private static final int TIME = 4;

        private static Vector columns = new Vector();
        static {
            columns.add("Select");
            columns.add("Batch");
            columns.add("Job Description");
            columns.add("Date");
            columns.add("Time");
        }

        public TableModelStep1(Vector data) {
            super(data, columns);
        }

        private Class[] types = new Class[] { java.lang.Boolean.class,
                java.lang.Integer.class, java.lang.Object.class,
                java.lang.String.class, java.lang.String.class };

        public Class getColumnClass(int columnIndex) {
            return types[columnIndex];
        }

        // only one row can be selected
        public boolean isCellEditable(int rowIndex, int columnIndex) {
            return columnIndex == SELECTED
                    && (Boolean.TRUE.equals(getValueAt(rowIndex, SELECTED)) || !isSelected());
        }

        private static final String DATE_FORMAT = "dd/MM/yyyy";

        private static final String TIME_FORMAT = "HH:mm:ss";

        public Object getValueAt(int rowIndex, int columnIndex) {
            Object value = super.getValueAt(rowIndex, columnIndex);
            if (columnIndex == DATE)
                return com.argus.util.DateTimeUtils.asString(
                        (java.util.Date) value, DATE_FORMAT);
            if (columnIndex == TIME)
                return com.argus.util.DateTimeUtils.asString(
                        (java.util.Date) value, TIME_FORMAT);
            return value;
        }

        public boolean isSelected() {
            for (int r = 0; r < getRowCount(); r++)
                if (Boolean.TRUE.equals(getValueAt(r, SELECTED)))
                    return true;
            return false;
        }

        public Collection getSelected() {
            Collection c = new ArrayList();
            for (int r = 0; r < getRowCount(); r++)
                if (Boolean.TRUE.equals(getValueAt(r, SELECTED)))
                    c.add(getValueAt(r, JOURNAL));
            return c;
        }

        public AssetProcessor.Journal getSelectedBatch() {
            for (int r = 0; r < getRowCount(); r++)
                if (Boolean.TRUE.equals(getValueAt(r, SELECTED)))
                    return (AssetProcessor.Journal) getValueAt(r, JOURNAL);
            return null;
        }

    }

