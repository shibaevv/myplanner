    protected void execute() {

        java.awt.Window w = SwingUtilities.windowForComponent(this);

        final SplashWindow splash = new SplashWindow(null,
                w instanceof java.awt.Frame ? (java.awt.Frame) w : null);
        splash.setStringPainted("Waiting for Assets Recover to start ...");
        splash.setVisible(true);

        new Thread(new Runnable() {
            public void run() {
                new Thread(splash, "SplashWindow").start();
                splash.setStringPainted("Assets Recover ...   Please Wait...");

                try {

                    Object whoIsChanged = null;
                    try {
                        int i = 0;
                        Iterator iter = tableModelStep1.getSelected()
                                .iterator();
                        while (iter.hasNext()) {
                            AssetProcessor.Journal journal = (AssetProcessor.Journal) iter
                                    .next();

                            splash.setStringPainted("Recover " + journal);

                            assetProcessor.executeRecover(journal);

                            whoIsChanged = journal;

                            // first one, one at a time
                            break;

                        }

                    } catch (Exception e) {
                        whoIsChanged = e;
                        e.printStackTrace(System.err);
                    }

                    // update changes done summary table
                    model.sendMessage(whoIsChanged, MessageSent.INFO,
                            AssetManagerModel.UPDATE3);

                    model.putValue(WIZARD, STEP_3);
                    model.sendNotification(new Object());

                } finally {
                    splash.close();
                }

            }
        }, "doImport").start();

    }

